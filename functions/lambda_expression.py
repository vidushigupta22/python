# if one wants to solve mathematical expressions like f(x) = x+x-3*5 -->this is maths
# f = lambda x : x+z-3*5 --> this is python
# f(5) ,, now x will take 5 value and solve for you
# and you can take any variable in python like instead of f one can take g,h,,i any alphabet

f = lambda x: x + x -3 * 5
print(f(3))
print(f(100))
print(f(1.23))

g = lambda x,y : (x*y) - (x//y)
print(g(3,4))
